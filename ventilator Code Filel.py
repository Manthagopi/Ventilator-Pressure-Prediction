import pandas as pd
import numpy as np
from lightgbm import LGBMRegressor

train_data = pd.read_csv('/content/drive/MyDrive/train.csv')
test_data = pd.read_csv('/content/drive/MyDrive/test.csv')

train_data

test_data

test_data.isnull().sum()
train_data.isnull().sum()

test_data_submissions_column = test_data['id']

feature_train = train_data.drop(columns = 'pressure',axis=1)
target_train = train_data['pressure']
feature_test = test_data

model = LGBMRegressor()
model.fit(feature_train,target_train)
y_prediction = model.predict(feature_test)

submission = pd.DataFrame(columns = ['id'],data=test_data_submissions_column)
submission['pressure'] = y_prediction
submission.head()

submission.to_csv('Kaggle_Pressure_submission',index=False)